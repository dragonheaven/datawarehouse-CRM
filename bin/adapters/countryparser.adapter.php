		public static function GET_COUNTRY( $input ) {
			switch ( true ) {
				case is_numeric( $input ) && $input == intval( $input ):
					$return = static::GET_BY_SPOTID( $input );
					break;

				case ! is_numeric( $input ) && 2 == strlen( $input ):
					$return = static::GET_BY_ISO( $input );
					break;

				case ! is_numeric( $input ) && 3 == strlen( $input ):
					$return = static::GET_BY_ISO3( $input );
					break;

				default:
					$return = static::GET_BY_NAME( ucwords( $input ) );
					break;
			}
			return $return;
		}

		private static function GET_BY_SPOTID( $input ) {
			$sid = absint( $input );
			$return = 'XX';
			if ( can_loop( static::$countries ) ) {
				foreach ( static::$countries as $value => $data ) {
					if ( $sid == absint( get_array_key( 'spotID', $data ) ) ) {
						$return = $value;
					}
				}
			}
			return $return;
		}

		private static function GET_BY_NAME( $name ) {
			$return = 'XX';
